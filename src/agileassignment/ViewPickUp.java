    public void addRowToTable(){
        DefaultTableModel model = (DefaultTableModel) jtbPickUpList.getModel();
        ArrayList<PickUp> list = PickUpList();
        Object rowData[] = new Object[8];
        
        for(int i = 0; i < list.size(); i++){
            rowData[0] = list.get(i).id;
            rowData[1] = list.get(i).name;
            rowData[2] = list.get(i).flower;
            rowData[3] = list.get(i).flowerType;
            rowData[4] = list.get(i).quantity;
            rowData[5] = list.get(i).totalPrice;
            rowData[6] = list.get(i).date;
            rowData[7] = list.get(i).status;
            
            model.addRow(rowData);
        }
    }
