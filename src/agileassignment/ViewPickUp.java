    public class PickUp{
        public String id;
        public String name;
        public String flower;
        public String flowerType;
        public int quantity;
        public double totalPrice;
        public String date;
        public String status;
        
        public PickUp(String id, String name, String flower, String flowerType, int quantity, double totalPrice, String date, String status){
            this.id = id;
            this.name = name;
            this.flower = flower;
            this.flowerType = flowerType;
            this.quantity = quantity;
            this.totalPrice = totalPrice;
            this.date = date;
            this.status = status;
        }
    }
    
